<?php

    // ==, !=, ===, !==

    $a = 5;
    $b = 3;

    // OP IGUALDADE
    if($a == $b) { //false
        echo "A é igual a B <br>";
    }

    // OP DIFERENÇA
    if($a != $b) { //true
        echo "A é diferente de B <br>";
    }

    // OP IDÊNTICO
    if($a === $b) { //false
        echo "A é idêntico a B <br>";
    }

    // OP NÃO IDÊNTICO
    if($a !== $b) { //true
        echo "A não é idêntico a B <br>";
    }

?>